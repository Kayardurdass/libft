#include "../../../include/libftmath.h"

void	ft_vec3_cross(t_vec3 a, t_vec3 b, t_vec3 dest)
{
	dest[0] = a[1] * b[2] - a[2] * b[1];
	dest[1] = a[2] * b[0] - a[0] * b[0];
	dest[2] = a[0] * b[1] - a[1] * b[0];
}
