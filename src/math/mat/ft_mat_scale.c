#include "../../../include/libftmath.h"

void	ft_mat2_scale(t_vec2 a, t_mat2 m)
{
	m[0][0] = a[0];
	m[1][1] = a[1];
}

void	ft_mat3_scale(t_vec3 a, t_mat3 m)
{
	m[0][0] = a[0];
	m[1][1] = a[1];
	m[2][2] = a[2];
}

void	ft_mat4_scale(t_vec4 a, t_mat4 m)
{
	m[0][0] = a[0];
	m[1][1] = a[1];
	m[2][2] = a[2];
	m[3][3] = a[3];
}
