#include "../../../include/libftmath.h"

void	ft_mat2_mulv(t_mat2 m, t_vec2 v, t_vec2 dest)
{
	dest[0] = m[0][0] * v[0] + m[0][1] * v[1];
	dest[1] = m[1][0] * v[0] + m[1][1] * v[1];
}

void	ft_mat3_mulv(t_mat3 m, t_vec3 v, t_vec3 dest)
{
	dest[0] = m[0][0] * v[0] + m[0][1] * v[1] + m[0][2] * v[2];
	dest[1] = m[1][0] * v[0] + m[1][1] * v[1] + m[1][2] * v[2];
	dest[2] = m[2][0] * v[0] + m[2][1] * v[1] + m[2][2] * v[2];
}

void	ft_mat4_mulv(t_mat4 m, t_vec4 v, t_vec4 dest)
{
	dest[0] = m[0][0] * v[0] + m[0][1] * v[1] + m[0][2] * v[2] + m[0][3] * v[3];
	dest[1] = m[1][0] * v[0] + m[1][1] * v[1] + m[1][2] * v[2] + m[1][3] * v[3];
	dest[2] = m[2][0] * v[0] + m[2][1] * v[1] + m[2][2] * v[2] + m[2][3] * v[3];
	dest[3] = m[3][0] * v[0] + m[3][1] * v[1] + m[3][2] * v[2] + m[3][3] * v[3];
}
