#include "../../../include/libftmath.h"

void	ft_mat2_copy(t_mat2 a, t_mat2 dest)
{
	dest[0][0] = a[0][0];
	dest[1][0] = a[1][0];
	dest[0][1] = a[0][1];
	dest[1][1] = a[1][1];
}

void	ft_mat3_copy(t_mat3 a, t_mat3 dest)
{
	dest[0][0] = a[0][0];
	dest[0][1] = a[0][1];
	dest[0][2] = a[0][2];
	dest[1][0] = a[1][0];
	dest[1][1] = a[1][1];
	dest[1][2] = a[1][2];
	dest[2][0] = a[2][0];
	dest[2][1] = a[2][1];
	dest[2][2] = a[2][2];
}

void	ft_mat4_copy(t_mat4 a, t_mat4 dest)
{
	dest[0][0] = a[0][0];
	dest[0][1] = a[0][1];
	dest[0][2] = a[0][2];
	dest[0][3] = a[0][3];
	dest[1][0] = a[1][0];
	dest[1][1] = a[1][1];
	dest[1][2] = a[1][2];
	dest[1][3] = a[1][3];
	dest[2][0] = a[2][0];
	dest[2][1] = a[2][1];
	dest[2][2] = a[2][2];
	dest[2][3] = a[2][3];
	dest[3][0] = a[3][0];
	dest[3][1] = a[3][1];
	dest[3][2] = a[3][2];
	dest[3][3] = a[3][3];
}
